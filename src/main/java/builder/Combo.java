package builder;

public class Combo {
	private String gaseosa;
	private String pipocas;
	private int chocolate;
	private int precio;
	public String getGaseosa() {
		return gaseosa;
	}
	public void setGaseosa(String gaseosa) {
		this.gaseosa = gaseosa;
	}
	public String getPipocas() {
		return pipocas;
	}
	public void setPipocas(String pipocas) {
		this.pipocas = pipocas;
	}
	public int getChocolate() {
		return chocolate;
	}
	public void setChocolate(int chocolate) {
		this.chocolate = chocolate;
	}
	public int getPrecio() {
		return precio;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	
	
}
